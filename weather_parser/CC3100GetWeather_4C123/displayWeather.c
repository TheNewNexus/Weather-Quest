void UpdateWeather(char* city, char* temp, char* minTemp, char* maxTemp,
                   char* humidity, char* weather) {
  
		SysTickIntDisable();
  // Wait for a short time to allow systick_count to change
 

  //srand(SysTickValueGet());  // Seed the random number generator with the
                             // current time

  int random_num = abs(rand()) % 10;  // Generate a random number between 0 and 9

  switch (random_num) {
    case 0:
      ST7735_FillScreen(ST7735_NAVY);
      break;
    case 1:
      ST7735_FillScreen(ST7735_DARKGREEN);
      break;
    case 2:
      ST7735_FillScreen(ST7735_MAROON);
      break;
    case 3:
      ST7735_FillScreen(ST7735_DARKCYAN);
      break;
    case 4:
      ST7735_FillScreen(ST7735_OLIVE);
      break;
    case 5:
      ST7735_FillScreen(ST7735_LIGHTBLUE);
      break;
    case 6:
      ST7735_FillScreen(ST7735_DARKGREY);
      break;
    case 7:
      ST7735_FillScreen(ST7735_SILVER);
      break;
    case 8:
      ST7735_FillScreen(ST7735_GOLD);
      break;
    case 9:
      ST7735_FillScreen(ST7735_BROWN);
      break;
  }

	ST7735_SetFont(Font);
  // City name (largest size, blue)
  ST7735_DrawString(0, 0, city, ST7735_BLUE, size2);
	
  //if city name is over 11 characters, shift the weather data down
  if(strlen(city) <= 11){	
  // Temperatures (smallest size, purple)
	ST7735_SetFont(Font_Thin);
  ST7735_DrawString(0, 2, "Min Temp: ", ST7735_MAGENTA, size1);
  ST7735_DrawString(10, 2, minTemp, ST7735_MAGENTA, size1);
  ST7735_DrawString(0, 3, "Max Temp: ", ST7735_MAGENTA, size1);
  ST7735_DrawString(10, 3, maxTemp, ST7735_MAGENTA, size1);
  ST7735_DrawString(0, 4, "Current Temp: ", ST7735_MAGENTA, size1);
  ST7735_DrawString(14, 4, temp, ST7735_MAGENTA, size1);
  // Humidity and weather (medium size, green and orange)
	ST7735_SetFont(Font_Bold); // Size 2
  ST7735_DrawString(0, 5, "Humidity: ", ST7735_ORANGE, size1);
  ST7735_DrawString(10, 5, humidity, ST7735_ORANGE, size1);
  ST7735_DrawString(0, 6, "Weather: ", ST7735_GREEN, size1);
  ST7735_DrawString(9, 6, weather, ST7735_GREEN, size1);
  }
  else{
    // Temperatures (smallest size, purple)
  ST7735_DrawString(0, 4, "Min Temp: ", ST7735_MAGENTA, size1);
  ST7735_DrawString(10, 4, minTemp, ST7735_MAGENTA, size1);
  ST7735_DrawString(0, 5, "Max Temp: ", ST7735_MAGENTA, size1);
  ST7735_DrawString(10, 5, maxTemp, ST7735_MAGENTA, size1);
  ST7735_DrawString(0, 6, "Current Temp: ", ST7735_MAGENTA, size1);
  ST7735_DrawString(14, 6, temp, ST7735_MAGENTA, size1);
  // Humidity and weather (medium size, green and orange)
	ST7735_SetFont(Font_Bold); // Size 2
  ST7735_DrawString(0, 7, "Humidity: ", ST7735_ORANGE, size1);
  ST7735_DrawString(10, 7, humidity, ST7735_ORANGE, size1);
  ST7735_DrawString(0, 8, "Weather: ", ST7735_GREEN, size1);
  ST7735_DrawString(9, 8, weather, ST7735_GREEN, size1);
  }

 SysTickIntEnable();
}

